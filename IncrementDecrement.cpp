#include<iostream>
using namespace std;

int main()
{
    int A = 11;
    int B = 21;
    int C = 51;
    int D = 101;

    A++; // increment
    ++B; // increment

    C--; // decrement
    --D; // decrement

    cout << A << " " << B << endl;
    cout << C << " " << D << endl;
    
    return 0;
}